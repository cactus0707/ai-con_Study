var subject = '자바스크립트';


switch (subject) {
    case "C언어":
        console.log('초보자를 위한 C 언어 200제');
        break;
        
    case "자바스크립트":
        console.log('초보자를 위한 자바스크립트 언어 200제');
        break;
    case "파이썬":
        console.log('초보자를 위한 파이썬 언어 200제');
        break;

    default:
        console.log('이젠 초보자가 아닙니다.');
        break;
}

